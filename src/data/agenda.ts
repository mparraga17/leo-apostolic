// Helper para obtener título/descripción/location según el idioma
// Si no hay versión EN, devuelve la española (fallback transparente).
export function localizeEvent(event: PapalEvent, locale: 'es' | 'en') {
  if (locale === 'en') {
    return {
      title: event.titleEn ?? event.title,
      description: event.descriptionEn ?? event.description,
      location: event.locationEn ?? event.location,
      registrationNote: event.registrationNoteEn ?? event.registrationNote,
    };
  }
  return {
    title: event.title,
    description: event.description,
    location: event.location,
    registrationNote: event.registrationNote,
  };
}

